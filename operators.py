a = 12
b = 3

print(a + b)    # 15
print(a - b)    # 9
print(a * b)    # 36
print(a / b)    # 4.0
print(a // b)   # 4 integer division, rounded down
print(a % b)    # remainder

print()

# values in range must be integers
for i in range(1, 4):
    print(i)

